    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        // TODO add your handling code here:
        try{
        String nombre= txtNombre.getText();
        String raza= txtRaza.getText();
        String padecimiento= txtPadecimiento.getText();
        double altura= Double.parseDouble(txtAltura.getText());
        double peso= Double.parseDouble(txtPeso.getText());
        int numVisitas= Integer.parseInt(txtNumVisitas.getText());
        
        Perro unPerro= new Perro(nombre,raza,padecimiento,altura,peso,numVisitas);
        listaPerros.add(unPerro);
        
        for(int i=0;i<listaPerros.size();i++){
            System.out.println(listaPerros.get(i));
        }
        contador=contador+1;
        JOptionPane.showMessageDialog(rootPane, contador+" guardado(s)");
        
        txtNombre.setText("");
        txtRaza.setText("");
        txtPadecimiento.setText("");
        txtAltura.setText("");
        txtPeso.setText("");
        txtNumVisitas.setText("");
        }
        catch(NumberFormatException e1){
            JOptionPane.showMessageDialog(rootPane, "ERROR: dato invÃ¡lido, Intente de nuevo");
            
        }
        
    }//GEN-LAST:event_btnGuardarMouseClicked

